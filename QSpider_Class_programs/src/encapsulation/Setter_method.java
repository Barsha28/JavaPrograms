package encapsulation;

public class Setter_method {
	private String s;
	private byte b;
	private char ch;
	
	public String getS() {
		return s;
	}
	public byte getB() {
		return b;
	}
	public char getCh() {
		return ch;
	}
	public void setS(String s) {
		this.s=s;
	}
	public void setB(byte b) {
		this.b=b;
	}
	public void setCh(char ch) {
		this.ch=ch;
	}
	
}
